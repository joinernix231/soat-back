            let newIndex = currentBanner + direction;
            
            if (newIndex < 0) {
                newIndex = totalBanners - 1;
            } else if (newIndex >= totalBanners) {
                newIndex = 0;
            }
            
            showBanner(newIndex);
            resetBannerInterval();
        }

